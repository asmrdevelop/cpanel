# Super class for Cpanel::Config::ConfigObj::Driver::* which provides a default
#  methods/functions to be overridden
use strict;
use warnings;

our $VERSION = 1.0;

use parent qw(Cpanel::Config::ConfigObj::Interface::Driver);

=head1 NAME

C<Cpanel::Config::ConfigObj::Interface::Whostmgr>

=head1 SYNOPSIS

  package Cpanel::Config::ConfigObj::Driver::CustomModule;

  use Cpanel::Imports;

  use parent qw(
    Cpanel::Config::ConfigObj::Interface::Config::v1
    Cpanel::Config::ConfigObj::Interface::Whostmgr
  )

  sub acl_desc {
      {
            acl           => 'custom_acl',
            acl_subcat    => locale()->maketext('Custom Extensions'),
            name          => locale()->maketext('Custom Application'),
            default_value => 0,
            is_plugin     => 1,
      }
  }

  sub contact_descriptions {
      my ($self, $kind) = @_;
      return {
          'Custom::SignupComplete' => {
            display_name => locale()->maketext('Signup Complete'),
            help_text    => locale()->maketext('This notification is sent once the Custom Applications Signup is complete.'),
            is_plugin    => 1,
            driver_name  => 'custom_application_name',
      } if $kind eq 'cpanel';
      return {};
  }

=head1 DESCRIPTION

An interface to implement when you are setting up a component for the cPanel Interface.

This interface provides a mechnism for users to extend WHM Interface control features for that cPanel Interface.

=head1 METHODS

=head2 INSTANCE->acl_desc()

Get the descriptions for features. Used in Feature Manager interface.

=cut

sub acl_desc {
    return [];
}

=head2 INSTANCE->contact_descriptions($KIND, $LOCALE)

Get any contact notification descriptions added by this component. Used in the WHM Contact Manager >> Notifications tab.

=head3 ARGUMENTS

=over

=item $KIND - string

One of C<whm> or C<cpanel>.

=item $LOCALE - Cpanel::Locale

The locale object used to localize any string generated by this call.

=back

=cut

sub contact_descriptions {
    my ( $self, $kind, $locale ) = @_;
    return {};
}

1;
